import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { categories, getPublishedNiches, validateNiche } from '../ecosistema-soluciones/content.js';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const ecosystemHtmlPath = path.join(root, 'ecosistema-soluciones', 'index.html');
const homeHtmlPath = path.join(root, 'index.html');
const ecosystemStartMarker = '          <!-- ECOSYSTEM_NICHES_START: generated by scripts/render-ecosystem.mjs -->';
const ecosystemEndMarker = '          <!-- ECOSYSTEM_NICHES_END -->';
const homeStartMarker = '    <!-- HOME_NICHES_START: generated by scripts/render-ecosystem.mjs -->';
const homeEndMarker = '    <!-- HOME_NICHES_END -->';

const escapeHtml = (value) => String(value).replace(/[&<>"']/g, (character) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' })[character]);

function renderImage(record) {
  const image = record.image;
  if (!image.src) return `<div class="niche-image-wrap fallback-image" data-motion-widget="SOL-W11"><div class="fallback-symbol" aria-hidden="true">3D</div><span class="image-badge">${escapeHtml(image.label || 'Sin imagen disponible')}</span></div>`;
  const prefix = image.src.replace(/\.webp$/, '');
  const srcset = `${prefix}-480.webp 480w, ${prefix}-768.webp 768w, ${image.src} 960w`;
  return `<div class="niche-image-wrap" data-motion-widget="SOL-W11"><img src="${escapeHtml(image.src)}" srcset="${escapeHtml(srcset)}" sizes="(max-width: 767px) calc(100vw - 32px), (max-width: 1200px) calc(50vw - 44px), 566px" alt="${escapeHtml(image.alt)}" width="${image.width}" height="${image.height}" loading="lazy" decoding="async"><span class="image-badge">${escapeHtml(image.label)}</span></div>`;
}

function renderGallery(record) {
  if (!record.gallery?.length) return '';
  const figures = record.gallery.map((image) => {
    const prefix = image.src.replace(/\.webp$/, '');
    const srcset = `${prefix}-480.webp 480w, ${prefix}-768.webp 768w, ${image.src} 960w`;
    return `<figure><div><img src="${escapeHtml(image.src)}" srcset="${escapeHtml(srcset)}" sizes="(max-width: 767px) calc(50vw - 28px), (max-width: 1200px) calc(25vw - 36px), 265px" alt="${escapeHtml(image.alt)}" width="${image.width}" height="${image.height}" loading="lazy" decoding="async"><span>${escapeHtml(image.label)}</span></div><figcaption>${escapeHtml(image.descriptor)}</figcaption></figure>`;
  }).join('');
  return `<div class="detail-gallery" aria-label="Ejemplos conceptuales para ${escapeHtml(record.name)}" data-motion-widget="SOL-W14">${figures}</div>`;
}

function renderApplications(items, className, limit = items.length, motionWidget = '') {
  const motionAttribute = motionWidget ? ` data-motion-widget="${motionWidget}"` : '';
  return `<ul class="${className}"${motionAttribute}>${items.slice(0, limit).map((item) => `<li data-application tabindex="0">${escapeHtml(item)}</li>`).join('')}</ul>`;
}

function renderCard(record) {
  const category = categories.find(({ id }) => id === record.categoryId);
  return [
    `          <article class="niche-card" data-niche-card data-niche="${escapeHtml(record.id)}" data-category="${escapeHtml(record.categoryId)}" data-image-type="${escapeHtml(record.image.type)}" data-quote-context="${escapeHtml(record.name)}" data-motion-widget="SOL-W10" data-motion-signature="spotlight-origin" id="nicho-${escapeHtml(record.slug)}">`,
    `            ${renderImage(record)}`,
    `            <div class="niche-card-body"><p class="card-kicker">${escapeHtml(category.name.toUpperCase())} · ${escapeHtml(record.name.toUpperCase())}</p><h3>${escapeHtml(record.name)}</h3><p class="niche-problem">${escapeHtml(record.problem)}</p><p class="niche-benefit">${escapeHtml(record.benefit)}</p>${renderApplications(record.applications, 'application-preview', 4)}<p class="personalization-line">${escapeHtml(record.personalizationNotice)}</p><button class="text-button niche-open" type="button" aria-expanded="false" aria-controls="detail-${escapeHtml(record.slug)}" data-motion-widget="SOL-W15">Ver posibilidades <span aria-hidden="true">→</span></button></div>`,
    `            <div class="niche-detail" id="detail-${escapeHtml(record.slug)}" data-motion-widget="SOL-W13" data-motion-signature="origin-expand" hidden>${renderGallery(record)}<div class="detail-inner"><div><p class="detail-label">EL PROBLEMA U OPORTUNIDAD</p><p>${escapeHtml(record.problem)}</p></div><div><p class="detail-label">APLICACIONES</p>${renderApplications(record.applications, 'application-list', record.applications.length, 'SOL-W12')}</div><div><p class="detail-label">BENEFICIO Y SIGUIENTE PASO</p><p>${escapeHtml(record.benefit)} ${escapeHtml(record.personalizationNotice)}</p><a class="detail-quote" href="#cotizacion" data-quote-trigger>${escapeHtml(record.cta.label)} <span aria-hidden="true">→</span></a></div><button class="detail-close" type="button" data-motion-widget="SOL-W15">Volver a los nichos</button></div></div>`,
    '          </article>',
  ].join('\n');
}

function renderHomeVisual(record) {
  if (record.image.src) {
    const src = record.image.src.replace('../assets/', 'assets/');
    return `<div class="home-niche-visual"><img src="${escapeHtml(src)}" alt="${escapeHtml(record.image.alt)}" width="960" height="720" loading="lazy" decoding="async"><span class="home-niche-badge">${escapeHtml(record.image.label)}</span></div>`;
  }
  return `<div class="home-niche-visual home-niche-visual--concept" aria-label="Representación conceptual para ${escapeHtml(record.name)}"><span class="concept-shape concept-shape--one" aria-hidden="true"></span><span class="concept-shape concept-shape--two" aria-hidden="true"></span><span class="concept-shape concept-shape--three" aria-hidden="true"></span><span class="home-niche-badge">Ejemplo conceptual</span><strong>${escapeHtml(record.name)}</strong><small>Ideas 3D personalizables</small></div>`;
}

function renderHomeCard(record) {
  const category = categories.find(({ id }) => id === record.categoryId);
  const applications = record.applications.slice(0, 3).map(escapeHtml).join(' · ');
  return [
    `        <a class="home-niche-card" href="/ecosistema-soluciones/?nicho=${escapeHtml(record.id)}#${escapeHtml(record.categoryId)}" data-home-niche="${escapeHtml(record.id)}">`,
    `          ${renderHomeVisual(record)}`,
    '          <div class="home-niche-card__body">',
    `            <p class="home-niche-category">${escapeHtml(category.name)}</p>`,
    `            <h3>${escapeHtml(record.name)}</h3>`,
    `            <p>${applications}</p>`,
    '            <span>Ver ideas para este nicho <span aria-hidden="true">→</span></span>',
    '          </div>',
    '        </a>',
  ].join('\n');
}

function renderHomeSection(records) {
  return [
    homeStartMarker,
    '    <section id="nichos" class="home-niches section-pad" aria-labelledby="home-niches-title">',
    '      <div class="home-niches__container">',
    '        <p class="section-eyebrow">SOLUCIONES PARA NEGOCIOS</p>',
    '        <h2 id="home-niches-title">¿Qué quieres crear para tu negocio?</h2>',
    '        <p class="home-niches__intro">Explora ejemplos por actividad. Son puntos de partida personalizables, no productos de catálogo ni proyectos reales.</p>',
    '        <div class="home-niches__grid">',
    records.map(renderHomeCard).join('\n'),
    '        </div>',
    '        <div class="home-niches__actions"><a href="/ecosistema-soluciones/" class="home-niches__cta">Explorar todo el ecosistema <span aria-hidden="true">→</span></a><a href="https://wa.me/528331080178" target="_blank" rel="noopener noreferrer" class="home-niches__whatsapp">Cotizar por WhatsApp</a></div>',
    '      </div>',
    '    </section>',
    homeEndMarker,
  ].join('\n');
}

function replaceGenerated(source, startMarker, endMarker, generated, label) {
  const start = source.indexOf(startMarker);
  const end = source.indexOf(endMarker);
  if (start < 0 || end < start) throw new Error(`No se encontraron los marcadores de ${label}.`);
  return { output: source.slice(0, start) + generated + source.slice(end + endMarker.length), current: source.slice(start, end + endMarker.length) };
}

const published = getPublishedNiches();
for (const record of published) {
  const validation = validateNiche(record);
  if (!validation.valid) throw new Error(`${record.id}: ${validation.errors.join(', ')}`);
}
const ecosystemHtml = fs.readFileSync(ecosystemHtmlPath, 'utf8');
let previousCategory = '';
const generatedCards = published.map((record) => {
  const anchor = record.categoryId !== previousCategory ? `          <span class="category-anchor" id="${escapeHtml(record.categoryId)}" aria-hidden="true"></span>\n` : '';
  previousCategory = record.categoryId;
  return anchor + renderCard(record);
});
const generatedEcosystem = `${ecosystemStartMarker}\n${generatedCards.join('\n\n')}\n${ecosystemEndMarker}`;
const ecosystemResult = replaceGenerated(ecosystemHtml, ecosystemStartMarker, ecosystemEndMarker, generatedEcosystem, 'contenido del ecosistema');
const homeHtml = fs.readFileSync(homeHtmlPath, 'utf8');
const generatedHome = renderHomeSection(published);
const homeResult = replaceGenerated(homeHtml, homeStartMarker, homeEndMarker, generatedHome, 'nichos de la home');
if (process.argv.includes('--check')) {
  if (ecosystemResult.current !== generatedEcosystem || homeResult.current !== generatedHome) {
    console.error('El HTML publicado no coincide con content.js. Ejecuta npm run content:render.');
    process.exitCode = 1;
  } else console.log(`${published.length} nichos publicados y sincronizados en ecosistema y home.`);
} else {
  fs.writeFileSync(ecosystemHtmlPath, ecosystemResult.output, 'utf8');
  fs.writeFileSync(homeHtmlPath, homeResult.output, 'utf8');
  console.log(`${published.length} nichos publicados en ecosistema y home.`);
}
